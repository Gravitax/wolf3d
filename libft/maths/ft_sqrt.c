float		ft_sqrt(float nb)
{
	float	x;
	int		i;

	if (nb <= 1)
		return (nb == 1 ? 1 : 0);
	nb = (float)nb;
	x = 1;
	i = 23;
	while (--i)
		x = (0.5) * (x + nb / x);
	return (x);
}
